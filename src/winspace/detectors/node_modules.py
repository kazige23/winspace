"""Detector: ``node_modules`` directories.

A bounded-depth BFS from a configurable root (default :func:`pathlib.Path.home`)
looking for any directory named ``node_modules`` that:

* is a real directory (not a reparse point — already-relocated node_modules
  would loop back through their junction)
* is not on the safety NEVER list
* is not nested inside another already-discovered node_modules (we stop
  descent at the first hit — that's where the size lives anyway)

Hidden directories (``.git``, ``.venv``, …) and well-known noise paths
like ``$Recycle.Bin`` are skipped for performance. The detector is
single-pass and avoids touching the file system any more than necessary.
"""

from __future__ import annotations

from pathlib import Path
from typing import ClassVar

from winspace.core.fs import FileSystem
from winspace.core.safety import is_never
from winspace.detectors.base import Candidate, Detector, RiskLevel

_DEFAULT_MAX_DEPTH = 6

# Names we never recurse into, even before checking the NEVER list. The
# NEVER list catches system paths, but these are user-tree noise where
# walking would burn time for nothing.
_SKIP_NAMES = frozenset(
    {
        "node_modules",  # already-known target; do not nest searches
        "$Recycle.Bin",
        "System Volume Information",
        ".git",
        ".hg",
        ".svn",
        ".venv",
        "venv",
        ".tox",
        ".mypy_cache",
        ".pytest_cache",
        ".ruff_cache",
        "__pycache__",
    }
)


class NodeModulesDetector(Detector):
    """Locate every relocatable ``node_modules`` under the configured roots."""

    name: ClassVar[str] = "node_modules"

    def __init__(
        self,
        *,
        roots: list[Path] | None = None,
        max_depth: int | None = None,
    ) -> None:
        self._roots: list[Path] = roots if roots is not None else [Path.home()]
        self._max_depth: int = max_depth if max_depth is not None else _DEFAULT_MAX_DEPTH

    def find(self, fs: FileSystem) -> list[Candidate]:
        results: list[Candidate] = []
        for root in self._roots:
            if not fs.exists(root) or not fs.is_dir(root):
                continue
            self._walk(fs, root, depth=0, results=results)
        return results

    def _walk(
        self,
        fs: FileSystem,
        current: Path,
        *,
        depth: int,
        results: list[Candidate],
    ) -> None:
        if depth > self._max_depth:
            return
        blocked, _ = is_never(current)
        if blocked:
            return
        if fs.is_reparse_point(current):
            return

        try:
            children = list(fs.iterdir(current))
        except (PermissionError, OSError):
            return

        for child in children:
            if not fs.is_dir(child):
                continue
            name = child.name
            if name == "node_modules":
                # Found a candidate. Don't descend into it — outermost wins.
                if fs.is_reparse_point(child):
                    continue
                child_blocked, _ = is_never(child)
                if child_blocked:
                    continue
                results.append(self._make_candidate(child))
                continue
            if name in _SKIP_NAMES or _is_hidden(name):
                continue
            self._walk(fs, child, depth=depth + 1, results=results)

    def _make_candidate(self, path: Path) -> Candidate:
        return Candidate(
            path=path,
            category="node_modules",
            risk=RiskLevel.SAFE,
            reason_zh="Node.js 依赖目录,移除后 `npm install` 可随时重建",
            reason_en="Node.js dependencies; regenerable via `npm install`",
            detector_name=self.name,
        )


def _is_hidden(name: str) -> bool:
    """Skip dotfiles/dirs except a small whitelist (currently empty)."""
    return name.startswith(".") and name != "."
