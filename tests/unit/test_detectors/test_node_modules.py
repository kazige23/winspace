"""Unit tests for :mod:`winspace.detectors.node_modules`.

The detector is exercised against synthetic directory trees in
``tmp_path`` so we don't depend on whatever happens to be in the
developer's home folder.
"""

from __future__ import annotations

from pathlib import Path
from typing import Any

import pytest

from winspace.core.fs import RealFileSystem
from winspace.detectors.base import RiskLevel
from winspace.detectors.node_modules import NodeModulesDetector

# --- helpers -----------------------------------------------------------------


def _make_tree(root: Path, layout: dict[str, Any]) -> None:
    """Recursively materialise ``layout``. ``None`` -> empty dir;
    ``str`` -> file with that content; ``dict`` -> sub-directory.
    """
    root.mkdir(parents=True, exist_ok=True)
    for name, value in layout.items():
        target = root / name
        if value is None:
            target.mkdir(exist_ok=True)
        elif isinstance(value, str):
            target.write_text(value)
        elif isinstance(value, dict):
            _make_tree(target, value)
        else:
            raise TypeError(f"unsupported layout value type: {type(value)}")


@pytest.fixture
def fs() -> RealFileSystem:
    return RealFileSystem()


# --- baseline ----------------------------------------------------------------


def test_empty_root_returns_no_candidates(fs: RealFileSystem, tmp_path: Path) -> None:
    det = NodeModulesDetector(roots=[tmp_path])
    assert det.find(fs) == []


def test_missing_root_handled(fs: RealFileSystem, tmp_path: Path) -> None:
    det = NodeModulesDetector(roots=[tmp_path / "no-such"])
    assert det.find(fs) == []


def test_root_is_file_handled(fs: RealFileSystem, tmp_path: Path) -> None:
    (tmp_path / "loose.txt").write_text("x")
    det = NodeModulesDetector(roots=[tmp_path / "loose.txt"])
    assert det.find(fs) == []


# --- finds at various depths -------------------------------------------------


def test_finds_node_modules_at_shallow_depth(fs: RealFileSystem, tmp_path: Path) -> None:
    _make_tree(tmp_path, {"project-a": {"node_modules": {"x": "1"}}})
    det = NodeModulesDetector(roots=[tmp_path])
    results = det.find(fs)
    assert len(results) == 1
    [c] = results
    assert c.path == tmp_path / "project-a" / "node_modules"
    assert c.category == "node_modules"
    assert c.risk == RiskLevel.SAFE
    assert c.detector_name == "node_modules"


def test_finds_multiple_node_modules_in_one_root(fs: RealFileSystem, tmp_path: Path) -> None:
    _make_tree(
        tmp_path,
        {
            "frontend": {"node_modules": {"a": "x"}},
            "backend": {"web": {"node_modules": {"b": "y"}}},
            "tools": {"deeper": {"and-again": {"node_modules": {"c": "z"}}}},
        },
    )
    det = NodeModulesDetector(roots=[tmp_path])
    results = det.find(fs)
    paths = sorted(c.path.relative_to(tmp_path).as_posix() for c in results)
    assert paths == [
        "backend/web/node_modules",
        "frontend/node_modules",
        "tools/deeper/and-again/node_modules",
    ]


# --- does NOT recurse into discovered node_modules --------------------------


def test_nested_node_modules_only_outer_returned(fs: RealFileSystem, tmp_path: Path) -> None:
    """A monorepo can have node_modules/foo/node_modules. We want only the
    outermost path because that's where the bulk of the bytes live and
    relocating it covers the children for free.
    """
    _make_tree(
        tmp_path,
        {
            "app": {
                "node_modules": {
                    "left-pad": {"node_modules": {"is-positive": "y"}},
                }
            }
        },
    )
    det = NodeModulesDetector(roots=[tmp_path])
    results = det.find(fs)
    assert len(results) == 1
    assert results[0].path == tmp_path / "app" / "node_modules"


# --- safety: junction-flavored node_modules skipped -------------------------


def test_skips_node_modules_that_is_a_reparse_point(
    fs: RealFileSystem, tmp_path: Path, monkeypatch: pytest.MonkeyPatch
) -> None:
    _make_tree(
        tmp_path,
        {"app": {"node_modules": {"foo": "1"}}, "other": {"node_modules": {"bar": "2"}}},
    )
    monkeypatch.setattr(
        RealFileSystem,
        "is_reparse_point",
        lambda self, p: p == tmp_path / "app" / "node_modules",
    )
    det = NodeModulesDetector(roots=[tmp_path])
    paths = [c.path.relative_to(tmp_path).as_posix() for c in det.find(fs)]
    assert paths == ["other/node_modules"]


def test_skips_root_that_is_a_reparse_point(
    fs: RealFileSystem, tmp_path: Path, monkeypatch: pytest.MonkeyPatch
) -> None:
    """If the configured root is itself a junction, we don't enter it."""
    _make_tree(tmp_path, {"project": {"node_modules": {"x": "1"}}})
    monkeypatch.setattr(RealFileSystem, "is_reparse_point", lambda self, p: p == tmp_path)
    det = NodeModulesDetector(roots=[tmp_path])
    assert det.find(fs) == []


# --- safety: NEVER paths skipped --------------------------------------------


def test_skips_paths_under_never_list(
    fs: RealFileSystem, tmp_path: Path, monkeypatch: pytest.MonkeyPatch
) -> None:
    """Synthesise NEVER matches for a specific path so we can test the
    safety integration without depending on real system folders.
    """
    import winspace.detectors.node_modules as nm_module

    _make_tree(
        tmp_path,
        {"safe": {"node_modules": {"a": "1"}}, "blocked": {"node_modules": {"b": "2"}}},
    )

    real_is_never = nm_module.is_never

    def fake_is_never(p: Path | str | None) -> tuple[bool, str]:
        if p is not None and "blocked" in str(p):
            return True, "synthetic"
        return real_is_never(p)

    monkeypatch.setattr(nm_module, "is_never", fake_is_never)
    det = NodeModulesDetector(roots=[tmp_path])
    paths = [c.path.relative_to(tmp_path).as_posix() for c in det.find(fs)]
    assert paths == ["safe/node_modules"]


