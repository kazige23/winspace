# --- max_depth limit applied ------------------------------------------------


def test_max_depth_blocks_deeply_nested_node_modules(fs: RealFileSystem, tmp_path: Path) -> None:
    # Build a chain a/b/c/d/e/f/node_modules — 7 levels deep.
    deep = tmp_path
    for d in ["a", "b", "c", "d", "e", "f"]:
        deep = deep / d
    _make_tree(deep, {"node_modules": {"y": "1"}})

    # max_depth=3 means the walk stops well before f/.
    det = NodeModulesDetector(roots=[tmp_path], max_depth=3)
    assert det.find(fs) == []

    # max_depth=10 is generous enough to reach it.
    det = NodeModulesDetector(roots=[tmp_path], max_depth=10)
    assert len(det.find(fs)) == 1


# --- error recovery ---------------------------------------------------------


def test_permission_error_on_subtree_handled(
    fs: RealFileSystem, tmp_path: Path, monkeypatch: pytest.MonkeyPatch
) -> None:
    _make_tree(
        tmp_path,
        {
            "good": {"node_modules": {"x": "1"}},
            "locked": {"node_modules": {"y": "1"}},
        },
    )
    real_iterdir = RealFileSystem.iterdir

    def flaky(self: RealFileSystem, path: Path) -> Any:
        if path.name == "locked":
            raise PermissionError("denied")
        return real_iterdir(self, path)

    monkeypatch.setattr(RealFileSystem, "iterdir", flaky)
    det = NodeModulesDetector(roots=[tmp_path])
    paths = [c.path.relative_to(tmp_path).as_posix() for c in det.find(fs)]
    assert paths == ["good/node_modules"]


# --- default constructor uses Path.home() -----------------------------------


def test_default_root_is_user_home() -> None:
    det = NodeModulesDetector()
    assert det._roots == [Path.home()]


# --- Candidate carries the right messaging ----------------------------------


def test_candidate_messaging_is_bilingual(fs: RealFileSystem, tmp_path: Path) -> None:
    _make_tree(tmp_path, {"proj": {"node_modules": {"x": "1"}}})
    det = NodeModulesDetector(roots=[tmp_path])
    [c] = det.find(fs)
    assert "Node.js" in c.reason_zh or "npm" in c.reason_zh
    assert "npm install" in c.reason_en
    assert c.size_bytes == 0  # detector leaves sizing to the CLI


# --- empty list of roots is benign ------------------------------------------


def test_empty_roots_returns_no_candidates(fs: RealFileSystem) -> None:
    det = NodeModulesDetector(roots=[])
    assert det.find(fs) == []
