"""Task 16 — node_modules end-to-end roundtrip integration test.

Builds a small but plausible node_modules tree, runs the complete
``scan → move → list → undo`` cycle through the CLI, and verifies that
the bytes after undo are byte-identical to the bytes before move.

This is the headline test for Checkpoint 1B. It exercises every Phase 1
core atom and every Phase 2 detector / mover / CLI path in a single
flow against a real filesystem (plus real ``mklink /J`` on Windows).
"""

from __future__ import annotations

import json
import os
from pathlib import Path

import pytest
from click.testing import CliRunner

from winspace.cli import main
from winspace.core.fs import RealFileSystem
from winspace.core.junction import is_junction
from winspace.core.manifest import EntryStatus, load
from winspace.core.verify import fingerprint

windows_only = pytest.mark.skipif(
    os.name != "nt", reason="full roundtrip needs mklink /J on Windows"
)


def _make_node_modules(root: Path) -> None:
    """Build a plausible node_modules layout.

    A few packages, each with a package.json + an index.js + maybe a
    nested dependency. The shapes mimic what an actual ``npm install``
    leaves behind so that fingerprint sensitivity is exercised on
    realistic directory depth.
    """
    pkgs = {
        "lodash": {"package.json": "{}", "index.js": "module.exports = {}"},
        "react": {
            "package.json": '{"version":"18.2.0"}',
            "index.js": "//react",
            "cjs": {"react.production.min.js": "x" * 12_000},
        },
        "is-positive": {
            "package.json": '{"version":"3.1.0"}',
            "index.js": "module.exports = n => n > 0",
        },
        ".bin": {"eslint": "#!/usr/bin/env node"},
    }
    root.mkdir(parents=True, exist_ok=True)
    for pkg, files in pkgs.items():
        pkg_dir = root / pkg
        pkg_dir.mkdir(parents=True, exist_ok=True)
        for name, content in files.items():
            if isinstance(content, dict):
                sub = pkg_dir / name
                sub.mkdir(parents=True, exist_ok=True)
                for sub_name, sub_content in content.items():
                    (sub / sub_name).write_text(sub_content)
            else:
                (pkg_dir / name).write_text(content)


@pytest.fixture
def env(tmp_path: Path, monkeypatch: pytest.MonkeyPatch) -> tuple[Path, Path, Path]:
    home = tmp_path / "home"
    drive_d = tmp_path / "drive_d"
    manifest_path = tmp_path / "manifest.json"
    home.mkdir()
    drive_d.mkdir()
    (home / "AppData" / "Local").mkdir(parents=True)
    (home / "AppData" / "Roaming").mkdir(parents=True)
    monkeypatch.setattr(Path, "home", classmethod(lambda cls: home))
    monkeypatch.setattr("winspace.core.manifest.default_manifest_path", lambda: manifest_path)
    monkeypatch.setenv("LOCALAPPDATA", str(home / "AppData" / "Local"))
    monkeypatch.setenv("APPDATA", str(home / "AppData" / "Roaming"))
    return home, drive_d, manifest_path


@windows_only
@pytest.mark.windows
def test_node_modules_full_roundtrip(env: tuple[Path, Path, Path]) -> None:
    home, drive_d, manifest_path = env
    src = home / "my-app" / "node_modules"
    _make_node_modules(src)

    # Baseline: capture fingerprint BEFORE any winspace operation.
    before = fingerprint(src, fs=RealFileSystem())
    assert before.file_count > 0
    assert before.total_bytes > 0

    runner = CliRunner()

    # --- step 1: scan finds the directory --------------------------------
    scan_result = runner.invoke(main, ["scan", "--min-size", "1B", "--json"])
    assert scan_result.exit_code == 0, scan_result.output
    scan_data = json.loads(scan_result.output)
    targets = [r for r in scan_data["results"] if r["category"] == "node_modules"]
    assert targets, "scan did not surface the node_modules target"
    assert Path(targets[0]["path"]) == src

    # --- step 2: move --------------------------------------------------
    move_result = runner.invoke(main, ["move", str(src), "--to", str(drive_d), "--yes"])
    assert move_result.exit_code == 0, move_result.output

    # Junction in place, target dir holds the data, manifest active.
    assert is_junction(src)
    assert (src / "react" / "index.js").read_text() == "//react"
    m = load(manifest_path)
    assert len(m.entries) == 1
    assert m.entries[0].status == EntryStatus.ACTIVE
    new_path = Path(m.entries[0].new_path)
    assert new_path.exists()
    after_move = fingerprint(new_path, fs=RealFileSystem())
    assert after_move == before, (
        f"fingerprint diverged after move:\nbefore={before}\nafter={after_move}"
    )

    # --- step 3: list shows the active entry -----------------------------
    list_result = runner.invoke(main, ["list", "--json"])
    assert list_result.exit_code == 0
    list_data = json.loads(list_result.output)
    [entry] = list_data["entries"]
    assert entry["health"] == "ok"
    assert entry["status"] == "active"
    assert entry["size_bytes"] == before.total_bytes

    # --- step 4: undo --------------------------------------------------
    undo_result = runner.invoke(main, ["undo", "--last", "--yes"])
    assert undo_result.exit_code == 0, undo_result.output

    assert not is_junction(src), "expected the junction to be gone after undo"
    assert not new_path.exists(), f"expected {new_path} to be removed during undo"

    # --- step 5: byte-for-byte verification ------------------------------
    after = fingerprint(src, fs=RealFileSystem())
    assert after == before, (
        f"fingerprint diverged after undo roundtrip:\nbefore={before}\nafter={after}"
    )

    # Manifest should reflect rolled_back status.
    m = load(manifest_path)
    [restored] = m.entries
    assert restored.status == EntryStatus.ROLLED_BACK
